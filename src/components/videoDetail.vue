<template>
  <div v-if="video" class="col-md-8">
    <div class="embed-responsive embed-responsive-16by9">
      <iframe class="embed-responsive-item" v-bind:src="videoUrl"></iframe>
    </div>

    <div class="details">
      <h4>{{ video.snippet.title }}</h4>
      <p>{{ video.snippet.description }}</p>
    </div>
  </div>
</template>

<script>
const BASE_URL = "https://www.youtube.com/embed/";
export default {
  name: "videoDetail",
  props: {
    video: Object,
  },
  computed: {
    videoUrl() {
      const { videoId } = this.video.id;
      return BASE_URL + videoId;
    },
  },
};
</script>

<style scoped>
.details {
  margin-top: 10px;
  padding: 10px;
  border: 1px solid #ddd;
  border-radius: 4px;
}
</style>